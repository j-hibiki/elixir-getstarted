# arithmetic operations

IO.puts 1+2         # => 3
IO.puts 5*5         # => 25
IO.puts 10/2        # => 5.0


# arithmetic poerations2

IO.puts div(10, 2)  # => 5
IO.puts div 10, 2   # => 5
IO.puts rem 10, 3   # => 1


# binary, octal and hexadecimal numbers

IO.puts 0b1010      # => 10
IO.puts 0o777       # => 511
IO.puts 0x1F        # => 31


# rounding

IO.puts round(3.58) # => 4
IO.puts trunc(3.58) # => 3